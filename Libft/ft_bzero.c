#include "libft.h"

void	ft_bzero(void *b, size_t len)
{
	unsigned char	*copy_b;
	size_t			i;

	copy_b = b;
	i = 0;
	while (i < len)
	{
		copy_b[i] = 0;
		i++;
	}
}
