int	ft_isalpha(int ASCII_VALUE)
{
	return ((ASCII_VALUE >= 'A' && ASCII_VALUE <= 'Z')
		|| (ASCII_VALUE >= 'a' && ASCII_VALUE <= 'z'));
}
